    private class Updater implements Runnable {
        @Override
        public void run() {
            if (errorTimer == true){
                if (timerCount <= 0){
                    addIncomeErrorLabel.setText(" ");
                    timerCount = 5;
                } else {
                    timerCount--;
                }
            }
        }
    }
    
